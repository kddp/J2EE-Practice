package com.uttara.s2;

import java.util.Date;

public class PostBean {
	String post;
	String from;
	int to;
	int fro;
	Date d;
	Date t;
	
		
	public int getFro() {
		return fro;
	}
	public void setFro(int fro) {
		this.fro = fro;
	}
	public Date getT() {
		return t;
	}
	public void setT(Date t) {
		this.t = t;
	}
	public String getPost() {
		return post;
	}
	public void setPost(String post) {
		this.post = post;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public int getTo() {
		return to;
	}
	public void setTo(int to) {
		this.to = to;
	}
	public Date getD() {
		return d;
	}
	public void setD(Date d) {
		this.d = d;
	}	
}
